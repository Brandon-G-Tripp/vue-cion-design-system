<template>
  <div>
    <div 
      v-if="description" 
      v-html="description"/>
    <description v-else />
    <component-item
      v-if="components" 
      v-for="component in components"
      :key="component.name"
      :component="component" />
    <router-view/>
  </div>
</template>

<script>
import ComponentItem from './ComponentItem'

export default {
  name: 'SectionPage',
  props: {
    section: {
      type: Object,
      required: true
    },
    components: {
      type: Array,
      default: null
    }
  },
  components: {
    ComponentItem
  },
  data() {
    return {
      description: null
    }
  },
  created() {
    const name = this.section.name.replace(' ', '')
    try {
      const mdFile = require(`../docs/${name}.md`).default
      this.$options.components.Description = mdFile
    } catch (err) {
      this.description = `<h1>${this.section.name}</h1>`
    }
  }
}
</script>
