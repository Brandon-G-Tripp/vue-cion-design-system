<template>
  <component 
    :is="tag"
    class="text"
    :class="`${color} ${size}${bold ? ' bold' : ''}`">
    <slot />
  </component>
</template>

<script>
/**
 * Text is used for styling and grouping paragraphs or words.
 * Defaults to a `p` tag. If nested inside of another text
 * component it defaults to a `span` tag.
 * @version 1.0.0
 */
export default {
  name: 'DsText',
  provide() {
    return {
      $parentText: this
    }
  },
  inject: {
    $parentText: {
      default: null
    }
  },
  props: {
    /**
     * The color used for the text.
     * `default, light, lighter, primary, inverse, success, warning, error`
     */
    color: {
      type: String,
      default() {
        return this.$parentText ? this.$parentText.color : 'default'
      },
      validator: value => {
        return value.match(
          /(default|light|lighter|primary|inverse|success|warning|error)/
        )
      }
    },
    /**
     * Whether the text is bold.
     */
    bold: {
      type: Boolean,
      default() {
        return this.$parentText ? this.$parentText.bold : false
      }
    },
    /**
     * The size used for the text.
     * `base, large, x-large, small, x-small`
     */
    size: {
      type: String,
      default() {
        return this.$parentText ? this.$parentText.size : 'base'
      },
      validator: value => {
        return value.match(/(base|large|x-large|small|x-small)/)
      }
    },
    /**
     * The html element name used for the text.
     */
    tag: {
      type: String,
      default() {
        return this.$parentText ? 'span' : 'p'
      }
    }
  }
}
</script>

